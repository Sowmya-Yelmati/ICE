public class StockBean {
 private String companyName;
 private String symbol;
 private String lastTradePrice;
 private String sharesOwned;
 private double marketValueOfStake;
/**
 * @return the companyName
 */
public String getCompanyName() {
	return companyName;
}
/**
 * @param companyName the companyName to set
 */
public void setCompanyName(String companyName) {
	this.companyName = companyName;
}
/**
 * @return the symbol
 */
public String getSymbol() {
	return symbol;
}
/**
 * @param symbol the symbol to set
 */
public void setSymbol(String symbol) {
	this.symbol = symbol;
}
/**
 * @return the lastTradePrice
 */
public String getLastTradePrice() {
	return lastTradePrice;
}
/**
 * @param string the lastTradePrice to set
 */
public void setLastTradePrice(String string) {
	this.lastTradePrice = string;
}
/**
 * @return the sharesOwned
 */
public String getSharesOwned() {
	return sharesOwned;
}
/**
 * @param sharesOwned the sharesOwned to set
 */
public void setSharesOwned(String sharesOwned) {
	this.sharesOwned = sharesOwned;
}
/**
 * @return the marketValueOfStake
 */
public double getMarketValueOfStake() {
	return marketValueOfStake;
}
/**
 * @param marketValueOfStake the marketValueOfStake to set
 */
public void setMarketValueOfStake(double marketValueOfStake) {
	this.marketValueOfStake = marketValueOfStake;
}
 
}
